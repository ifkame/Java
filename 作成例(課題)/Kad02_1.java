public class Kad02_1{
	public static void main(String[] args){
		System.out.println("10 + 2 = " + (10 + 2));
		System.out.println("10 - 2 = " + (10 - 2));
		System.out.println("10 * 2 = " + 10 * 2);
		System.out.println("10 / 2 = " + 10 / 2);
		System.out.println("10 % 2 = " + 10 % 2);
		System.out.println("10.5 + 20.3 * 2 = " + (10.5 + 20.3) * 2);
	}
}